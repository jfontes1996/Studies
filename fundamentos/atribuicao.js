// = é uma atribuição sempre o simbolo da operação aritmética vem antes do = ou seja /= *= %=
const a = 7
let b = 3
//atribuição aditiva (NÃO É SUBSTITUIÇÃO DE VALOR DE A POR B)
b += a // b = b + a 
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 2 // % atribuição modular b = b % 2
console.log(b)
